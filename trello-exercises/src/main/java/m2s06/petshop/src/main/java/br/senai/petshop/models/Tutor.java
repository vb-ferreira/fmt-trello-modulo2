package br.senai.petshop.models;

public class Tutor {
	
	  // instance attributes
	  private int id;
	  private String nome;

	  // getters & setters
	  public int getId() {
	    return id;
	  }

	  public void setId(int id) {
	    this.id = id;
	  }

	  public String getNome() {
	    return nome;
	  }

	  public void setNome(String nome) {
	    this.nome = nome;
	  }

}
